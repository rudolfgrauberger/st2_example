package com.example.RequestBodys;

public class GerichtRequest {
    private String name;
    private int preis;

    public GerichtRequest() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPreis() {
        return preis;
    }

    public void setPreis(int preis) {
        this.preis = preis;
    }
}
